    private List<Persona> lista = new ArrayList<Persona>();
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        Persona p1 = new Persona();
        
        
        String nombre = this.txtNombre.getText();
        String cedula = this.txtCedula.getText();
        p1.setCedula(cedula);
        p1.setNombre(nombre);
        
        lista.add(p1);
        actLista();
        JOptionPane.showMessageDialog(rootPane, "Guardado exitoso");
       
        //this.listClientes;
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = this.listClientes.getSelectedIndex();
        
        lista.remove(indice);
        actLista();
        JOptionPane.showMessageDialog(rootPane, "Eliminado exitoso");
        
    }//GEN-LAST:event_btnEliminarActionPerformed

