    private void actLista(){
        DefaultListModel datos = new DefaultListModel();
        for(int i =0; i < lista.size(); i++){
            Persona p1 = lista.get(i);
            datos.addElement(p1.getNombre());
        }
        this.listClientes.setModel(datos);
    }
    
    
